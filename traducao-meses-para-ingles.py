mes = input("Digite o nome do mês: ").lower()

if mes == "janeiro":
  print("January")
elif mes == "fevereiro":
  print("February")
elif mes == "março":
  print("March")
elif mes == "abril":
  print("April")
elif mes == "maio":
  print("May")
elif mes == "junho":
  print("June") 
elif mes == "julho":
  print("July") 
elif mes == "agosto":
  print("August")
elif mes == "setembro":
  print("September")
elif mes == "outubro":
  print("October")
elif mes == "novembro":
  print("November")
elif mes == "dezembro":
  print("December")
else:
  print("Operação Inválida!")
